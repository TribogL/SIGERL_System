    // Renderer para que las categor√≠as se vean distinto
    private static class CategoryRenderer extends javax.swing.DefaultListCellRenderer {

        @Override
        public java.awt.Component getListCellRendererComponent(
                javax.swing.JList<?> list, Object value, int index,
                boolean isSelected, boolean cellHasFocus) {

            super.getListCellRendererComponent(list, value, index, isSelected, cellHasFocus);

            if (value == null) {
                return this;
            }

            String text = value.toString();

            // Estilo especial para los separadores: "‚Äî ... ‚Äî"
            if (text.startsWith("‚Äî") && text.endsWith("‚Äî")) {
                setFont(getFont().deriveFont(java.awt.Font.BOLD));
                setForeground(new java.awt.Color(80, 80, 80));
                setBackground(new java.awt.Color(220, 220, 220));
            }

            return this;
        }
    }
    // Detecta si el item es una categor√≠a (separador)

    private boolean isCategoryItem(Object value) {
        if (value == null) {
            return false;
        }
        String text = value.toString();
        return text.startsWith("‚Äî") && text.endsWith("‚Äî");
    }

